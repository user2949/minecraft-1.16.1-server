public class gk implements gj {
	protected final double a;
	protected final double b;
	protected final double c;

	public gk(double double1, double double2, double double3) {
		this.a = double1;
		this.b = double2;
		this.c = double3;
	}

	@Override
	public double a() {
		return this.a;
	}

	@Override
	public double b() {
		return this.b;
	}

	@Override
	public double c() {
		return this.c;
	}
}
